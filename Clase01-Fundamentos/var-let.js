// Declaración de "var", en una sola línea
var musica = ("Rock");
console.log(musica);
// Declaración de "var", en dos líneas
var musica;
musica = ("Rock");
console.log(musica);
// Declaración de "var", en un bloque
{
    var musica = ("Pop")
    console.log(musica)
}
// Declaración de "var", después de un bloque
console.log(musica)

// Declaración de "let", en una sola línea
let num = ("100");
console.log(num);
// Declaración de "let", en dos líneas
let = num;
console.log(num)
{
let num = ("200");
console.log(num); 
}
// Declaración de "let", después del bloque
console.log(num);